package com.example.myfunctions;

public class Record {
    private int times;
    private int timeu;
    private int score;

    public int getTimes() {
        return times;
    }

    public void setTimes(int times) {
        this.times = times;
    }

    public int getTimeu() {
        return timeu;
    }

    public void setTimeu(int timeu) {
        this.timeu = timeu;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

}
